namespace Project_Akhir.Models
{
    public class Donatur
    {
        
        // Encapsulation
        private string nama;
        private string noTelp;
        private string email;

        public string Nama
        {
            get { return nama; }
            set { nama = value; }
        }

        public string NoTelp
        {
            get { return noTelp; }
            set { noTelp = value; }
        }

        public string Email
        {
            get { return email; }
            set { email = value; }
        }
    }
}

